using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Website2.Class
{
    public class Math2
    {
        public int TinhTong(int a,int b)
        {
            return a + b;
        }
        public int TinhTich(int a,int b)
        {
            return a * b;
        }
        public int TinhHieu(int a,int b)
        {
            return a - b;
        }
        public float TinhThuong(float a,float b)
        {
            return (float)(a / b);
        }

    }
}